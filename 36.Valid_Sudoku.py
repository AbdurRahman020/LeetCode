class Solution(object):
    def isValidSudoku(self, board: list[list[str]]) -> bool:
        for row in board:
            num = []
            for col in row:
                if col.isdigit():
                    if col in num:
                        return False
                    num.append(col)
        
        for col in range(9):
            num = []
            for row in range(9):
                n = board[row][col]
                if n.isdigit():
                    if n in num:
                        return False
                    num.append(n)
        
        for row in range(0,9,3):
            for col in range(0,9,3):
                num = []
                for r in range(row, row+3):
                    for c in range(col, col+3):
                        n = board[r][c]
                        if n.isdigit():
                            if n in num:
                                return False
                            num.append(n)
        return True

